package com.quanshu.exchange.support.api.base;

public enum ApiCode {
    SUCCESS(0, "请求成功"),
    FAILURE(1, "请求失败");

    private int code;
    private String msg;

    ApiCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}